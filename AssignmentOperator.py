#Define Initial Values
a = 10
b = 5

#Assignment
assignment = a; print("a =", assignment)

#Addition Assignment
a += b; print("a after +=", a)

#Subtraction Assignment
a -= b; print("a after -=", a)

#Multiplication Assignment
a *= b; print("a after *=", a)

#Division Assignment
a /= b; print("a after /=", a)

#Integer Division Assignment
a //= b; print("a after //=", a)

#Modulus Assignment
a %= b; print("a after %=", a)

#Exponentiation Assignment
a **= b; print("a after **=", a)